Que signifie "alumni" ?</br></br>
Le terme alumni aujourd'hui utilisé internationalement provient du mot latin alumni (pluriel de alumnus, élèves présents et passés). Il était au départ utilisé dans les pays anglo-saxons, en particulier au Royaume-Uni et aux États-Unis où les associations d'anciens élèves des universités portent le nom d'alumni association, puis par extension, le terme alumni a été également appliqué aux regroupements d'anciens membres d'une organisation (une entreprise par exemple).</br></br>

