<div class="titre-text">
    <h2> <span id="interrogation-couleur">H</span>istoire </h2>
    <p> L’IFA propose des formations (en initial ou en alternance) sur Metz depuis 1986, de niveau BAC, BAC +2, BAC +3 et jusqu’à BAC +5.</p>
    <p> A chaque rentrée, ce sont plus de 400 stagiaires qui effectuent leur stage dans une entreprise et intègrent un cursus d’apprentissage leur permettant d’obtenir un diplôme.</p>
    <p> Le centre de formation IFA (Institut Français des Affaires) propose des formations professionnelles dans les domaines du tertiaire: commerce, marketing, management, communication, ressources humaines, gestion, comptabilité, banque, finance et informatique. Les formations de l’IFA donnent accès à des titres reconnus par l’Etat ou des diplômes de l’Education Nationale. </p>
    <p> L’Institut Français des Affaires dispose d’une équipe de plus de 100 consultants, professionnels et formateurs, reconnus pour leur expérience professionnelle, ce qui fait de l’IFA un établissement réputé en Moselle. L’IFA accueille des stagiaires de toute la Lorraine, aussi bien de Metz que de Nancy. </p>
